# 4) for loop ( საიტერაციო ცვლადს დაარქვით num1 ) გამოვიყენოთ და შიგნით ჩავაშენოთ(შიგნით კიდევ შევქმანთ) for loop (საიტერაციო ცვლადს დაარქვით num)  და დაპრიმტეთ მეორე ლუპში ორივე, საიტერაციო ცვლადის მნიშნვნელობა


for num1 in range  (10):
    for num2 in range (10,20):
        print(num1, num2)
